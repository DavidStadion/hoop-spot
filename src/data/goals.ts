import type { Goal } from './types';

function validate(goals: Goal[]): Goal[] {
  for (const g of goals) {
    const last = g.buildup[g.buildup.length - 1];
    if (!last?.isGoal) throw new Error(`Goal ${g.id}: last waypoint must have isGoal: true`);
    if (g.options.length !== 4) throw new Error(`Goal ${g.id}: options must have 4 entries`);
    if (!g.options.includes(g.scorer)) throw new Error(`Goal ${g.id}: options must include scorer`);
  }
  return goals;
}

